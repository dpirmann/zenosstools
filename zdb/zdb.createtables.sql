CREATE TABLE `zendevices` (
  `name` varchar(256) NOT NULL,
  `id` text NOT NULL,
  `device` text NOT NULL,
  `uid` varchar(512) NOT NULL,
  `uuid` varchar(100) NOT NULL,
  `ipa` varchar(16) NOT NULL,
  `location` varchar(16) NOT NULL,
  `deviceclass` varchar(256) NOT NULL,
  `osmodel` text NOT NULL,
  `hwman` text NOT NULL,
  `hwmodel` text NOT NULL,
  `collector` varchar(256) NOT NULL,
  `comments` text NOT NULL,
  `description` text NOT NULL,
  `cpucount` int(11) NOT NULL,
  `ram` varchar(10) NOT NULL,
  `swap` varchar(10) NOT NULL,
  `fstotal` int(11) NOT NULL,
  `firstseen` int(11) NOT NULL,
  `lastchanged` int(11) NOT NULL,
  `lastcollected` int(11) NOT NULL,
  `dbupdated` int(11) NOT NULL,
  `prodstate` varchar(30) NOT NULL,
  `prodstateval` int(11) NOT NULL,
  `priority` varchar(20) NOT NULL,
  `priorityval` int(11) NOT NULL,
  `serial` text NOT NULL,
  `snmpcomm` text NOT NULL,
  `snmpdescr` text NOT NULL,
  `snmpsysname` text NOT NULL,
  `snmpversion` text NOT NULL,
  `groups` text NOT NULL,
  `systems` text NOT NULL,
  `uptime` bigint(20) NOT NULL,
  `windomain` text NOT NULL,
  `dnsfqdn` text NOT NULL,
  `billsizeval` int(11) NOT NULL,
  `billsize` varchar(10) NOT NULL,
  PRIMARY KEY (`name`),
  UNIQUE KEY `uid` (`uid`),
  UNIQUE KEY `uuid` (`uuid`),
  KEY `ipa` (`ipa`,`location`,`deviceclass`,`collector`,`prodstate`)
) ENGINE=InnoDB DEFAULT CHARSET=latin1;

CREATE TABLE `zendevattr` (
  `type` varchar(10) NOT NULL,
  `hostname` varchar(256) NOT NULL,
  `value` varchar(256) NOT NULL,
  `label` text NOT NULL,
  `dbupdated` int(11) NOT NULL,
  KEY `type` (`type`,`hostname`,`value`)
) ENGINE=InnoDB DEFAULT CHARSET=latin1;
